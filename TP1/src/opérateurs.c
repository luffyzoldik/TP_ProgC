#include <stdio.h>

int main() {
    int a = 16;
    int b = 3;
    
    printf("a = %d\n", a);
    printf("b = %d\n\n", b);
    
    printf("a + b = %d\n", a + b);
    printf("a - b = %d\n", a - b);
    printf("a * b = %d\n", a * b);
    printf("a / b = %d\n", a / b);
    printf("a %% b = %d\n\n", a % b);
    printf("a == b : %d\n", a == b);
    printf("a > b : %d\n", a > b);
    
    return 0;
}
